    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        NetworkDetails network = (NetworkDetails) cboxNetwork.getSelectedItem();
        EnterpriseDetails.EnterpriseType type = (EnterpriseDetails.EnterpriseType) cboxEnterprise.getSelectedItem();

        if (network == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }

        String name = txtName.getText();

        RescueCenterEntDetails enterprise = new RescueCenterEntDetails(name);
        Main.controller.saveObject(enterprise);
        network.getEnterpriseDirectory().add(enterprise);
        Main.controller.saveOrUpdate(network);

        populateTable();
    }//GEN-LAST:event_btnSubmitActionPerformed

