    private void txtNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNameKeyReleased
        String name  = txtName.getText();
        NetworkDetails network = (NetworkDetails) cboxNetwork.getSelectedItem();
        if (!this.validations.ValidateEmpty(name)) {
            valName.setText("Enterprise name is required");
        }
        else if (network.isEnterpriseExistByName(name)) {
            valName.setText("Enterprise already exist");
        }
        else {
            valName.setText(null);
        }
    }//GEN-LAST:event_txtNameKeyReleased

