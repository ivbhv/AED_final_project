    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblEnterprise.getModel();

        model.setRowCount(0);
        for (NetworkDetails network : new NetworkDetails().getNetworkList()) {
            for (EnterpriseDetails enterprise : network.getEnterpriseDirectory()) {
                Object[] row = new Object[3];
                row[0] = enterprise.getName();
                row[1] = network.getName();
                row[2] = enterprise.getEnterpriseType().getValue();

                model.addRow(row);
            }
        }
    }

    private void populateComboBox() {
        cboxNetwork.removeAllItems();
        cboxEnterprise.removeAllItems();

        for (NetworkDetails network : new NetworkDetails().getNetworkList()) {
            cboxNetwork.addItem(network);
        }

        for (EnterpriseDetails.EnterpriseType type : EnterpriseDetails.EnterpriseType.values()) {
            cboxEnterprise.addItem(type);
        }
    }
